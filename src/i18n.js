const fallbackLanguage = 'en';
const languageCodes = LANGUAGES.map(language => language.code);
const I18nContext = createContext(null);

function interpolate(text, params) {
  return Object.entries(params).reduce(
    (value, [key, replacement]) => value.replaceAll(`{${key}}`, replacement),
    text
  );
}

export function LanguageProvider({ children }) {
  const [language, setLanguage] = useState(() => {
    const saved = localStorage.getItem('masjid-finder-language');
    return languageCodes.includes(saved) ? saved : fallbackLanguage;
  });

  const currentLanguage = LANGUAGES.find(item => item.code === language) || LANGUAGES[0];

  useEffect(() => {
    localStorage.setItem('masjid-finder-language', language);
    document.documentElement.lang = language;
    document.documentElement.dir = currentLanguage.dir;
  }, [currentLanguage.dir, language]);

  const value = useMemo(() => {
    const dictionary = translations[language] || translations[fallbackLanguage];
    const fallback = translations[fallbackLanguage];

    return {
      language,
      languages: LANGUAGES,
      dir: currentLanguage.dir,
      setLanguage,
      t(key, params = {}) {
        const phrase = dictionary[key] ?? fallback[key] ?? key;
        if (typeof phrase === 'function') return phrase(params);
        return interpolate(phrase, params);
      },
    };
  }, [currentLanguage.dir, language]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useTranslation() {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error('useTranslation must be used within LanguageProvider');
  }
  return context;
}

export function getLocalizedMasjidName(masjid, language) {
  return masjid.nameTranslations?.[language] || masjid.name;
}
